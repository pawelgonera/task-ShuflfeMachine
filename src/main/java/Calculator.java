public class Calculator
{
    public static int add(Integer numberOne, Integer numberTwo)
    {
        return numberOne + numberTwo;
    }

    public static int subtract(Integer numberOne, Integer numberTwo)
    {
        return numberOne - numberTwo;
    }

    public static int multiply(Integer numberOne, Integer numberTwo)
    {
        return numberOne * numberTwo;
    }

    public static Float divide(Integer numberOne, Integer numberTwo)
    {
        return numberOne / (float) numberTwo;
    }
}
